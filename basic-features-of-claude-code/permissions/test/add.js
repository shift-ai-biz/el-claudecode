/**
 * 2つの数値を足し算する関数
 * @param {number} a - 1つ目の数値
 * @param {number} b - 2つ目の数値
 * @returns {number} a と b の合計
 */
export function add(a, b) {
    return a + b;
}

/**
 * 2つの数値を掛け算する関数
 * @param {number} a - 1つ目の数値
 * @param {number} b - 2つ目の数値
 * @returns {number} a と b の積
 */
export function multiply(a, b) {
    return a * b;
}
